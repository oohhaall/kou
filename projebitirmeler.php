				<h3>Araştırma Problemleri ve Bitirme Çalışmaları </h3>
						

			    <p><span class="style11">Araştırma Problemleri / Bitirme Tezi Dersi  Seçiminde İzlenecek Adımlar:</span><a name="_GoBack"></a><br>
                  <span class="style11">1)&nbsp;</span> İlgili  dönem itibariyle Araştırma Problemleri / Bitirme Projesi Derslerini verecek  hocaların kontenjanları eşit sayıda olacak şekilde belirlenir.<br>
			    </p>
			    <p>                  <span class="style11">2)</span> Bitirme komisyonu tarafından hocalara ait  kontenjan sayılarını ve çalışma konularını içeren bir duyuru yayınlanır. Proje  konuları en az iki kişilik öğrenci gruplarına uygun olacak şekilde belirlenir.<br>
                  </p>
			    <p><span class="style11">3)</span> Öğrenciler Araştırma Problemleri / Bitirme  Projesi Dersleri ile ilgili akademik takvimde yer alan ekle/çıkar haftasından  önce çalışmayı düşündükleri hocalar ile iletişime geçerler.<br>
                  </p>
			    <p><span class="style11">4)</span> Ders ekle/çıkar haftası içerisinde herhangi bir  hoca ile anlaşmamış olan öğrenciler danışmanları tarafından boşta kontenjanı  olan hocalara atanır.<br>
                  </p>
			    <p><span class="style11">5) </span>Derslerin başlangıcı ile birlikte hoca  tarafından atılan duyuru ile birlikte yapılan toplantıda herhangi bir proje  grubuna dâhil olmayan öğrenciler, hoca tarafından bir proje grubuna atanır.  (Projelerin en az iki kişilik gruplar halinde yapılması zorunludur.)</p>
			    <p><br>
                  <span class="style11">6) </span>2016-2017 Güz dönemi itibariyle ders planında 7.  Dönem Projesi olarak yer alan dersin ismi, Araştırma Problemleri olarak  değiştirilmiştir. </p>
			    <p><br>
			      <span class="style11">7) </span>Araştırma Problemleri dersinde daha önce verilen  7. Dönem Projesi dersinden farklı olarak, öğrencilerden sekizinci dönemde  verilen Bitirme Çalışması dersinin projesi için gerekli ön çalışmaların  yapılması beklenmektedir.</p>
			    <p class="style11">&nbsp;</p>
			    <p class="style11">Araştırma Problemleri ya da  Bitirme Tezi Tesliminde İzlenecek Yollar:</p>
			    <p><span class="style11">1) </span><strong>EK-1</strong>'deki Tez/Proje Şablonunu indiriniz. Çalışmalarınızı bu şablon üzerinden yapmanız size kolaylık 
			      sağlayacaktır. Şablon detaylarını görebilmek için Word'bayrak işaretçisini' aktif hale getiriniz. Şablonda problem yaşamanız durumunda, <strong>EK-2</strong> ve <strong>EK-3</strong>'teki gerekli açıklamaları okuyunuz.<br>
		        </p>
			    <p><span class="style11">2)</span> Ciltlettirme aşamasından önce çıktı alıp danışman hocanıza gösteriniz. Bu aşama, danışman hocanızın isteği doğrultusunda 
			      farklılık gösterebilir. Dikkat edilmesi gereken bir nokta; Proje  ya da tezinizi   <strong>PDF</strong> formatında kaydederek çıktı alınız.
			      Aksi durumda sayfanızda kaymalar olabilecektir.<br>
		        </p>
			    <p><span class="style11">3) </span><strong>EK-4</strong>'teki sizin için uygun olan Jüri Ortak Raporunu bilgisayar ortamında doldurunuz. Size ait olan bölümleri doldurunuz.
			      Bu sayfa, ciltleme aşamasında proje ya da tezin içerinde yer alan ÖNSÖZ ve TEŞEKKÜR bölümünden önce olacak şekilde projeye/teze eklenecektir.<br>
		        </p>
			    <p><span class="style11">4)</span> <strong>EK-5</strong>'te proje/tez kapak ve sırt bilgileri bulunmaktadır. Ciltleleme aşamasında bu bilgileri de projeye ya da teze ekleyiniz/ekletiniz. 
			      Bu bilgiler proje/tez içeriğine göre ve proje/tez yürütücüsüne göre düzenlenmelidir.<br>
		        </p>
			    <p><strong>Not</strong>: Dönem projeleri 1 tane beyaz karton kapaklı olacaktır. Bitirme tezleri ise 1 tane mavi bez ciltli olacaktır.</p>

				
